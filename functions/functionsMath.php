<?php
    /* d)*/

    function somma(float $nombre1, float $nombre2) : float{
        return $nombre1 + $nombre2; 
    }

    function multiplication(float $nombre1, float $nombre2) : float { 
        return $nombre1 * $nombre2;
    }

    function division(float $nombre1, float $nombre2) : float{
        return $nombre1/$nombre2;
    }

    function soustraction(float $nombre1, float $nombre2) : float{
        return $nombre1 - $nombre2;
    }




?>